package com.webank.defibus.common.exception;

public class DeFiBusException extends Exception {

    private int errorCode;
    private String errorString;

    public DeFiBusException(int errorCode, String errorString) {
        super("CODE: " + errorCode + " DESC:" + errorString);
        this.errorCode = errorCode;
        this.errorString = errorString;
    }

    public DeFiBusException(String message, Throwable cause) {
        super(message, cause);
        this.errorCode = -1;
        this.errorString = message;
    }

    public int getErrorCode() {
        return errorCode;
    }

    public void setErrorCode(int errorCode) {
        this.errorCode = errorCode;
    }

    public String getErrorString() {
        return errorString;
    }

    public void setErrorString(String errorString) {
        this.errorString = errorString;
    }
}
